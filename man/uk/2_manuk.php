<b>Загальне поняття:</b><br>
<p>«Интернет-Ферма» - це не просто окреме програмне забезпечення для комп’ютера на фермі. Це цілісний комплекс програмних, електронних та механічних приладів та вузлів, які спеціально налаштовані для синхронної та злагодженої роботи з метою здійснення постійного, автоматичного, ефективного управління вашим господарством. Комплекс працює в автономному режимі та практично не потребує втручання з боку персоналу господарства. Комплекс самостійно виконує збирання, впорядкування та занесення оперативної інформації до бази даних. Комплекс обробляє одержані дані та автоматично формує звіти за заданими користувачем параметрами.</p>
