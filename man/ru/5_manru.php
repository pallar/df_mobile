<b>Период:</b><br>
<p>Вся отчётная информация из базы данных может быть ограничена указанным пользователем периодом (раздел меню `Период`).</p>
<b>Раздел меню `Картотека`:</b><br>
<p>Каждое животное имеет собственную электронную <a href='javascript:{object=window.open("files/101_03ru.jpg","windowname","menubar=0,location=0,toolbar=0,resizable=0,scrollbars=0");object.focus()}'>карточку</a>, в которую можно внести паспортную часть данных, а именно: номер животного, дату рождения, код транспондера, дату регистрации транспондера, кличку, информацию про надои вне доильного зала, данные предков, принадлежность к определённой <a href='javascript:{object=window.open("files/501_61ru.jpg","windowname","menubar=0,location=0,toolbar=0,resizable=0,scrollbars=0");object.focus()}'>ферме</a> (<a href='javascript:{object=window.open("files/501_62ru.jpg","windowname","menubar=0,location=0,toolbar=0,resizable=0,scrollbars=0");object.focus()}'>секции</a>, подгруппе, <a href='javascript:{object=window.open("files/501_64ru.jpg","windowname","menubar=0,location=0,toolbar=0,resizable=0,scrollbars=0");object.focus()}'>породе</a>).</p>
<p>Картотека содержит следующие категории:
<ul>
<li><a href='javascript:{object=window.open("files/501_66ru.jpg","windowname","menubar=0,location=0,toolbar=0,resizable=0,scrollbars=0");object.focus()}'>карточки быков</a>;</li>
<li><a href='javascript:{object=window.open("files/501_65ru.jpg","windowname","menubar=0,location=0,toolbar=0,resizable=0,scrollbars=0");object.focus()}'>карточки коров (тёлок)</a>;</li>
<li><a href='javascript:{object=window.open("files/501_61ru.jpg","windowname","menubar=0,location=0,toolbar=0,resizable=0,scrollbars=0");object.focus()}'>карточки групп животных</a>.</li>
</ul>
</p>
<p>Каждая категория содержит сокращённые данные. Для просмотра детальной информации по конкретному животному (корове/тёлке, быку, группе) необходимо навести указатель "мыши" на соответствующий номер в таблице и щелкнуть один раз левой кнопкой "мыши". Откроется окно карточки, детализация которой регулируется специальными переключателями в верхней части.</p>
<b>Режим использования системы:</b><br>
<p>Компьютер, входящий в состав системы «Интернет-Ферма», предназначен для круглосуточной работы. В случаях кратковременного отсутствия напряжения его робота обеспечивается источником бесперебойного питания (ИБП). Если напряжение отсутствует более двух минут, то выполняется автоматическое отключение компьютера. В таком случае персонал должен самостоятельно включить компьютер.</p>
<p><i>ВНИМАНИЕ! Не рекомендуется использовать систему з нерабочим ИБП или вообще без него. Сохранность Ваших данных в этом случае не гарантируется.</i></p>
<p>С 02:30 и до 02:45 по Вашему локальному времени выполняются регламентные работы по обслуживанию базы данных и файловой системы компьютера. В этот период времени (технический перерыв) работа персонала с данными и работа в доильном зале должны быть приостановлены.</p>
