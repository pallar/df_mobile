<li><a href='javascript:{object=window.open("files/501_43ru.jpg","windowname","menubar=0,location=0,toolbar=0,resizable=0,scrollbars=0");object.focus()}'>Общие;</li>
<li><a href='javascript:{object=window.open("files/501_44ru.jpg","windowname","menubar=0,location=0,toolbar=0,resizable=0,scrollbars=0");object.focus()}'>Здоровье</a>;</li>
<li><a href='javascript:{object=window.open("files/501_45ru.jpg","windowname","menubar=0,location=0,toolbar=0,resizable=0,scrollbars=0");object.focus()}'>Графики</a>.</li>
</ul>
</p>
<p><a href='javascript:{object=window.open("files/501_01ru.jpg","windowname","menubar=0,location=0,toolbar=0,resizable=0,scrollbars=0");object.focus()}'>"Фильтры"</a> можно использовать и в этом разделе.</p>
